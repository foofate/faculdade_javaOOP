    private void btMostrarDespesasCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMostrarDespesasCategoriaActionPerformed
        // TODO add your handling code here:
        switch (cbCategorias.getSelectedIndex()) {

            case 0:
                categoria = Categoria.ALIMENTACAO;
                break;

            case 1:
                categoria = Categoria.LAZER;
                break;

            case 2:
                categoria = Categoria.VESTUARIO;
                break;

            case 3:
                categoria = Categoria.TRANSPORTE;
                break;

            case 4:
                categoria = Categoria.EDUCACAO;
                break;

            case 5:
                categoria = Categoria.OUTRAS;
                break;
        }
        
        taDespesasCategoria.setText(DespesaDAO.totalDespesasMes(categoria));
    }//GEN-LAST:event_btMostrarDespesasCategoriaActionPerformed

