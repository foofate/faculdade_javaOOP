    private void btAddDespesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddDespesaActionPerformed
        // TODO add your handling code here:
        switch (cbCategorias.getSelectedIndex()) {

            case 0:
                categoria = Categoria.ALIMENTACAO;
                break;

            case 1:
                categoria = Categoria.LAZER;
                break;

            case 2:
                categoria = Categoria.VESTUARIO;
                break;

            case 3:
                categoria = Categoria.TRANSPORTE;
                break;

            case 4:
                categoria = Categoria.EDUCACAO;
                break;

            case 5:
                categoria = Categoria.OUTRAS;
                break;
        }

        valor = Double.parseDouble(tfValor.getText());
        descricao = tfDescricao.getText();

        DespesaDAO.create(new Despesa(valor, categoria, descricao));
        
        tfTotalAtual.setText("R$ " + DespesaDAO.totalDespesas());
        tfValor.setText("");
        tfDescricao.setText("");
       
        
        


    }//GEN-LAST:event_btAddDespesaActionPerformed

