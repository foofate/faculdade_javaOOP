    private void btRecuperarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRecuperarActionPerformed
        if (LixeiraDAO.recuperar()) {
            LixeiraDAO.recuperar();
        }
        
        if (LixeiraDAO.getLixeira() != null) {
            tfAltura.setText(LixeiraDAO.getLixeira().getAltura() + "");
            tfRaio.setText(LixeiraDAO.getLixeira().getRaio() + "");
            tfArea.setText(LixeiraDAO.getLixeira().area() + "");
            tfVolumeMaximo.setText(LixeiraDAO.getLixeira().volumeMaximo() + "");
            tfVolumeAtual.setText(LixeiraDAO.getLixeira().getVolumeAtual() + "");
        }
    }//GEN-LAST:event_btRecuperarActionPerformed

