    private void btEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEnviarActionPerformed
        raio = Double.parseDouble(tfRaio.getText());
        altura = Double.parseDouble(tfAltura.getText());
        Lixeira lixeira = new Lixeira(altura, raio);
        LixeiraDAO.create(lixeira);

        tfArea.setText(LixeiraDAO.getLixeira().area() + "");
        tfVolumeMaximo.setText(LixeiraDAO.getLixeira().volumeMaximo() + "");
        tfVolumeAtual.setText(LixeiraDAO.getLixeira().getVolumeAtual() + "");
    }//GEN-LAST:event_btEnviarActionPerformed

